package control;
import java.lang.*;
public class Operator {
	private string email;
	private string password;
	
	/**
	 * Constructor for the operator
	 * @param e is the operator's email address
	 * @param p is the operator's password
	 */
	public Operator(String e, String p ) {
		email = e;
		password = p;
	}
	
	
}
